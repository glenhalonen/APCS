
/**
 * Constructor for equilateral trianlges. Simply sets all sides of a trianlge to the same length.
 * 
 * @author Glen Halonen
 * @version 02/12/2017
 */
public class Equilateral extends Triangle
{
    /**
     * Constructor for equilateral triangles.
     */
    public Equilateral(double side)
    {
        super(side, side, side);
    }
}
